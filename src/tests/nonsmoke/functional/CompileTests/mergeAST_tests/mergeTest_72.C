// This is a demo of how important the type is for variable declarations
// in un-named classes.
// Fixing the generation of names for un-named classes allowed this work.
typedef long int __off_t;
typedef long int __off64_t;

typedef struct
{
  __off_t __pos;
} _G_fpos_t;


typedef struct
{
  __off64_t __pos;
} _G_fpos64_t;

