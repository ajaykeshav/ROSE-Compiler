static _Bool
simple_atoi (const char *beg, const char *end, int *dest)
{
  int result = 0;
  _Bool negative = 0;
  const char *p = beg;

  while (p < end && c_isspace (*p))
    ++p;
  if (p < end && (*p == '-' || *p == '+'))
    {
      negative = (*p == '-');
      ++p;
    }
  if (p == end)
    return 0;




  if (!negative)
    for (; p < end && c_isdigit (*p); p++)
      {
        int next = (10 * result) + (*p - '0');
        if (next < result)
          return 0;
        result = next;
      }
  else
    for (; p < end && c_isdigit (*p); p++)
      {
        int next = (10 * result) - (*p - '0');
        if (next > result)
          return 0;
        result = next;
      }

  if (p != end)
    return 0;

  *dest = result;
  return 1;
}






static _Bool
simple_atof (const char *beg, const char *end, double *dest)
{
  double result = 0;

  _Bool negative = 0;
  _Bool seen_dot = 0;
  _Bool seen_digit = 0;
  double divider = 1;

  const char *p = beg;

  while (p < end && c_isspace (*p))
    ++p;
  if (p < end && (*p == '-' || *p == '+'))
    {
      negative = (*p == '-');
      ++p;
    }

  for (; p < end; p++)
    {
      char ch = *p;
      if (c_isdigit (ch))
        {
          if (!seen_dot)
            result = (10 * result) + (ch - '0');
          else
            result += (ch - '0') / (divider *= 10);
          seen_digit = 1;
        }
      else if (ch == '.')
        {
          if (!seen_dot)
            seen_dot = 1;
          else
            return 0;
        }
      else
        return 0;
    }
  if (!seen_digit)
    return 0;
  if (negative)
    result = -result;

  *dest = result;
  return 1;
}





